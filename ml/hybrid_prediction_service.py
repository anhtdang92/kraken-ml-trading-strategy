#!/usr/bin/env python3
"""
Hybrid Prediction Service for Stock ML Trading Dashboard

Combines real ML models with explicit fallback behavior.
Supports all stock categories: tech, sector leaders, defensive, ETFs, growth.

v2.0 improvements:
- MC Dropout uncertainty-based confidence instead of heuristic
- Proper scaler loading (training-set-only normalization)
- Ensemble model support
- Baseline comparison passthrough
- CRITICAL: Mock predictions are opt-in only (allow_mock parameter).
  By default, the service returns an explicit 'no_model' status instead
  of silently falling back to random noise. This prevents trading on
  fake predictions.
"""

import os
import logging
from typing import Dict, List, Optional
from datetime import datetime

from ml.prediction_service import PredictionService, DEFAULT_SYMBOLS
try:
    from gcp.deployment.vertex_prediction_service import VertexPredictionService
except ImportError:
    VertexPredictionService = None

logger = logging.getLogger(__name__)


class HybridPredictionService:
    """Hybrid prediction service combining real ML models with explicit fallbacks.

    By default, predictions that cannot be generated by a real ML model
    return status='no_model' with zero confidence. Mock predictions are
    only returned when allow_mock=True (for dashboard display only).
    """

    def __init__(self, models_dir: str = "models"):
        self.models_dir = models_dir
        self.models_status = {}

        self.enhanced_service = PredictionService(provider="local", models_dir=models_dir)
        self.vertex_service = None
        self.local_ml_models = {}

        self._init_vertex_ai()
        self._init_local_ml_models()

        logger.info("Hybrid Prediction Service v2.0 initialized")
        logger.info(f"   Vertex AI: {'Available' if self.vertex_service else 'Not available'}")
        logger.info(f"   Local ML Models: {len(self.local_ml_models)} found")

    def _init_vertex_ai(self):
        try:
            if VertexPredictionService is None:
                return

            project_id = os.getenv('GOOGLE_CLOUD_PROJECT', 'stock-ml-trading-487')
            region = os.getenv('GCP_REGION', 'us-central1')
            endpoint_id = os.getenv('VERTEX_ENDPOINT_ID', '')

            if endpoint_id:
                self.vertex_service = VertexPredictionService(
                    project_id=project_id, region=region, endpoint_id=endpoint_id
                )
        except Exception as e:
            logger.warning(f"Vertex AI initialization failed: {e}")

    def _init_local_ml_models(self):
        try:
            if os.path.exists(self.models_dir):
                model_files = [f for f in os.listdir(self.models_dir) if f.endswith('.h5')]
                for model_file in model_files:
                    symbol = model_file.replace('_model.h5', '').replace('_ensemble_0', '').upper()
                    model_path = os.path.join(self.models_dir, model_file)
                    if symbol not in self.local_ml_models:
                        self.local_ml_models[symbol] = {
                            'path': model_path, 'type': 'lstm', 'loaded': False
                        }
                if self.local_ml_models:
                    logger.info(f"Found {len(self.local_ml_models)} local ML models")
        except Exception as e:
            logger.warning(f"Local ML model initialization failed: {e}")

    def get_prediction(self, symbol: str, days_ahead: int = 21, allow_mock: bool = False) -> Dict:
        """Get prediction using hybrid approach.

        Priority: Vertex AI -> Local ML (with MC Dropout confidence) -> Explicit failure

        Args:
            symbol: Stock ticker.
            days_ahead: Prediction horizon.
            allow_mock: If True, fall back to mock predictions for dashboard display.
                        Default False — returns 'no_model' status for trade safety.
        """
        logger.info(f"Getting hybrid prediction for {symbol} ({days_ahead} days ahead, allow_mock={allow_mock})")

        # Try Vertex AI
        if self.vertex_service:
            try:
                ml_prediction = self.vertex_service.get_prediction(symbol, days_ahead)
                if ml_prediction and ml_prediction.get('status') == 'success':
                    ml_prediction['prediction_source'] = 'vertex_ai_ml'
                    ml_prediction['prediction_type'] = 'real_ml'
                    return ml_prediction
            except Exception as e:
                logger.warning(f"Vertex AI error for {symbol}: {e}")

        # Try local ML with MC Dropout confidence
        if symbol in self.local_ml_models:
            try:
                local_prediction = self._predict_with_local_model(symbol, days_ahead)
                if local_prediction:
                    local_prediction['prediction_source'] = 'local_ml'
                    local_prediction['prediction_type'] = 'real_ml'
                    return local_prediction
            except Exception as e:
                logger.warning(f"Local ML error for {symbol}: {e}")

        # No real ML model available
        if allow_mock:
            try:
                mock_prediction = self.enhanced_service.get_prediction(
                    symbol, days_ahead, allow_mock=True
                )
                mock_prediction['prediction_source'] = 'technical_analysis'
                mock_prediction['prediction_type'] = 'mock'
                mock_prediction['warning'] = (
                    'MOCK PREDICTION — this is NOT a real ML prediction. '
                    'Do not use for trade decisions.'
                )
                return mock_prediction
            except Exception as e:
                logger.warning(f"Mock prediction error for {symbol}: {e}")

        # Explicit failure — no silent fallback
        return {
            'symbol': symbol,
            'current_price': 0.0,
            'predicted_price': 0.0,
            'predicted_return': 0.0,
            'confidence': 0.0,
            'days_ahead': days_ahead,
            'status': 'no_model',
            'prediction_source': 'none',
            'prediction_type': 'none',
            'message': (
                f'No trained ML model available for {symbol}. '
                f'Train a model first with: prediction_service.train_model("{symbol}"). '
                'Mock predictions are disabled by default for trade safety.'
            ),
            'timestamp': datetime.now().isoformat(),
        }

    def _predict_with_local_model(self, symbol: str, days_ahead: int) -> Optional[Dict]:
        """Load a trained local LSTM model and generate prediction with MC Dropout."""
        try:
            from ml.lstm_model import StockLSTM, HAS_TENSORFLOW
            if not HAS_TENSORFLOW:
                return None

            from ml.feature_engineering import FeatureEngineer
            from ml.historical_data_fetcher import HistoricalDataFetcher

            model_info = self.local_ml_models.get(symbol)
            if not model_info or not os.path.exists(model_info['path']):
                return None

            fe = FeatureEngineer()
            fetcher = HistoricalDataFetcher()
            df = fetcher.fetch_historical_data(symbol, days=365)
            if df is None or len(df) < 100:
                return None

            df_features = fe.calculate_features(df)

            # Load training-set scaler to avoid data leakage
            scaler_params = self.enhanced_service._load_scaler(symbol)
            if scaler_params:
                df_normalized = fe.normalize_features(df_features, fit=False, scaler_params=scaler_params)
            else:
                df_normalized = fe.normalize_features(df_features, fit=True)

            num_features = len(fe.features)
            model = StockLSTM(lookback=30, num_features=num_features,
                              lstm_units=64, dropout_rate=0.2, l2_reg=1e-4)
            model.load_model(model_info['path'])

            feature_data = df_normalized[fe.features].values
            if len(feature_data) < 30:
                return None
            last_sequence = feature_data[-30:].reshape(1, 30, -1)

            # MC Dropout for principled uncertainty estimation
            import numpy as np
            mean_pred, std_pred = model.predict_with_uncertainty(last_sequence, n_forward_passes=50)
            predicted_return = float(mean_pred[0])
            prediction_uncertainty = float(std_pred[0])

            current_price = float(df['close'].iloc[-1])
            predicted_price = current_price * (1 + predicted_return)

            # Confidence from MC Dropout (lower uncertainty = higher confidence)
            confidence = float(np.clip(1.0 - prediction_uncertainty * 10, 0.1, 0.95))

            return {
                'symbol': symbol,
                'current_price': current_price,
                'predicted_price': predicted_price,
                'predicted_return': predicted_return,
                'confidence': confidence,
                'prediction_uncertainty': prediction_uncertainty,
                'days_ahead': days_ahead,
                'status': 'success',
                'timestamp': datetime.now().isoformat(),
            }
        except Exception as e:
            logger.warning(f"Local ML prediction failed for {symbol}: {e}")
            return None

    def get_all_predictions(self, symbols: List[str] = None, days_ahead: int = 21,
                            allow_mock: bool = False) -> Dict[str, Dict]:
        """Get predictions for all symbols.

        Args:
            symbols: List of ticker symbols (default: all tracked).
            days_ahead: Prediction horizon.
            allow_mock: If True, include mock predictions for symbols without models.
        """
        if symbols is None:
            symbols = DEFAULT_SYMBOLS

        logger.info(f"Getting hybrid predictions for {len(symbols)} symbols (allow_mock={allow_mock})")

        predictions = {}
        source_counts = {'real_ml': 0, 'mock': 0, 'no_model': 0}

        for symbol in symbols:
            prediction = self.get_prediction(symbol, days_ahead, allow_mock=allow_mock)
            predictions[symbol] = prediction
            prediction_type = prediction.get('prediction_type', 'no_model')
            source_counts[prediction_type] = source_counts.get(prediction_type, 0) + 1

        logger.info(f"Generated {len(predictions)} predictions: {source_counts}")
        return predictions

    def _has_model(self, symbol: str) -> bool:
        if symbol in self.local_ml_models:
            return True
        model_path = os.path.join(self.models_dir, f"{symbol}_model.h5")
        return os.path.exists(model_path)

    def train_model(self, symbol: str, days: int = 730, epochs: int = 150, save_model: bool = True) -> Dict:
        try:
            return self.enhanced_service.train_model(symbol, days, epochs, save_model)
        except Exception as e:
            return {'status': 'error', 'message': f'Training failed: {e}', 'symbol': symbol}

    def train_all_models(self, days: int = 730, epochs: int = 150) -> Dict:
        try:
            return self.enhanced_service.train_all_models(days, epochs)
        except Exception as e:
            return {'status': 'error', 'message': f'Training failed: {e}', 'results': {}}

    def get_prediction_summary(self) -> Dict:
        return {
            'vertex_ai_available': self.vertex_service is not None,
            'local_ml_models_available': len(self.local_ml_models) > 0,
            'local_ml_models_count': len(self.local_ml_models),
            'supported_symbols': DEFAULT_SYMBOLS,
            'mock_predictions_default': False,
            'system_status': 'hybrid_operational'
        }
